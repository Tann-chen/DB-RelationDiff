package test;

public class TupleByte {
	public int id;
	public byte[] firstName;
	public byte[] LastName;
	public int department;
	public int program;
	public int sin;
	public byte[] address;
	
	public TupleByte(int id, byte[] first, byte[] last, int department, int program, int sin, byte[] add) {
		this.id = id;
		this.firstName = first;
		this.LastName = last;
		this.department = department;
		this.program = program;
		this.sin = sin;
		this.address = add;
	}
	
	
}
